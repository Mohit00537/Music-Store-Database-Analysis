-- 1 What is the total revenue generated by the music store?
SELECT SUM(total) AS total_revenue FROM invoices;

-- 2 How many customers are there country-wise and which country has the highest number of customers?
SELECT country, COUNT(*) AS total_customers
FROM customers
GROUP BY country;

-- 3 Which tracks are the best-selling and how many times were they sold?
SELECT t.track_name, COUNT(*) AS total_sold
FROM invoice_items i
JOIN tracks t ON i.track_id = t.track_id
GROUP BY t.track_name
ORDER BY total_sold DESC;

-- 4 Which artist generates the highest revenue for the music store?
SELECT a.artist_name, SUM(inv.total) AS revenue
FROM invoices inv
JOIN invoice_items ii ON inv.invoice_id = ii.invoice_id
JOIN tracks t ON ii.track_id = t.track_id
JOIN albums al ON t.album_id = al.album_id
JOIN artists a ON al.artist_id = a.artist_id
GROUP BY a.artist_name
ORDER BY revenue DESC;

-- 5 Which music genre is the most popular among customers?
SELECT g.genre_name, COUNT(*) AS total_sold
FROM invoice_items ii
JOIN tracks t ON ii.track_id = t.track_id
JOIN genre g ON t.genre_id = g.genre_id
GROUP BY g.genre_name
ORDER BY total_sold DESC;

-- 6 How does the monthly sales performance look and in which month was the highest revenue generated?
SELECT MONTH(invoice_date) AS month, SUM(total) AS revenue
FROM invoices
GROUP BY MONTH(invoice_date);















